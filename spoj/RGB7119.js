function findGrad(x) {
  if (x == 8 || x == 9 || x == 10) {
    console.log("Autum");
  } else if (x == 11 || x == 12 || x == 1) {
    console.log("Winter");
  } else if (x == 2 || x == 3 || x == 4) {
    console.log("Spring");
  } else if (x == 5 || x == 6 || x == 7) {
    console.log("Summer");
  } else {
    console.log("Error");
  }
}
findGrad(9);
