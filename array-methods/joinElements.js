function joinElements() {
  let myColor = ["Red", "Green", "White", "Black"];
  let joinColor = myColor.join();
  console.log(joinColor);
}
joinElements();

function joinElement() {
  let myColor = ["Red", "Green", "White", "Black"];
  let joinColor = myColor.join("+");
  console.log(joinColor);
}
joinElement();

